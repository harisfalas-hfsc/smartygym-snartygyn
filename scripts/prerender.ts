/**
 * Build-time pre-renderer.
 * Runs after `vite build` and writes real static HTML for every public
 * route, with unique <title>, <meta description>, canonical, OG tags, JSON-LD
 * and real crawlable body content inside <div id="root">.
 *
 * Google sees the static HTML on first response (no JavaScript required).
 * React's createRoot then hydrates and renders the live app on top.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync, rmSync, statSync } from "node:fs";
import { dirname, resolve, join } from "node:path";
import { fileURLToPath } from "node:url";
import { buildSeoRoutes, canonicalPathFor } from "./lib/seo-routes";
import {
  applyHeadOverrides,
  injectBody,
  injectJsonLd,
  renderRouteBody,
} from "./lib/seo-render";

const DIST = resolve("dist");
const TEMPLATE_PATH = join(DIST, "index.html");

function normalizeTemplate(html: string) {
  return html
    .replace(/<script\s+type=["']application\/ld\+json["']\s+data-prerender=["']1["']>[\s\S]*?<\/script>\s*/gi, "")
    .replace(/<div id="root">\s*<main[\s\S]*?<\/main>\s*<\/div>/i, '<div id="root"></div>');
}

function ensureParentDirectoryForFile(outPath: string) {
  const parentDir = dirname(outPath);
  if (existsSync(parentDir) && !statSync(parentDir).isDirectory()) {
    rmSync(parentDir, { force: true });
  }
  mkdirSync(parentDir, { recursive: true });
}

function writeHeadersFile(distDir: string) {
  // Force HTML MIME type for every prerendered .html artifact (all non-root
  // canonical URLs end in .html in Option A).
  const lines: string[] = [
    "# Generated by scripts/prerender.ts",
    "",
    "/*.html",
    "  Content-Type: text/html; charset=utf-8",
    "  X-Robots-Tag: index, follow",
    "",
    "/",
    "  Content-Type: text/html; charset=utf-8",
    "  X-Robots-Tag: index, follow",
    "",
  ];
  writeFileSync(join(distDir, "_headers"), lines.join("\n"));
}

function writeCleanUrlRewrites(
  distDir: string,
  routePaths: string[],
  redirects: Array<{ from: string; to: string; status: 301 }> = [],
) {
  // Option B: canonical is the clean URL, served by the nested
  // `<path>/index.html` artifact. We only emit the legacy ID→slug 301s
  // here. Lovable hosting does not honor `_redirects`, so this file is
  // purely a fallback for hosts that DO process it (Netlify, mirrors).
  const seen = new Set<string>();
  const rules: string[] = [];
  for (const rule of redirects.sort((a, b) => a.from.localeCompare(b.from))) {
    const key = `R:${rule.from}>${rule.to}`;
    if (seen.has(key) || rule.from === rule.to) continue;
    seen.add(key);
    const target = canonicalPathFor(rule.to);
    const legacySources = new Set([rule.from, `${rule.from}/`]);
    if (!rule.from.endsWith(".html")) legacySources.add(`${rule.from}.html`);
    for (const source of legacySources) rules.push(`${source} ${target} ${rule.status}!`);
  }
  // Redirect old `.html` URLs to their clean canonical for hosts that
  // honor `_redirects`. Harmless on Lovable since both URLs serve the
  // same prerendered content.
  for (const p of [...routePaths].sort()) {
    if (p === "/") continue;
    const k = `H:${p}`;
    if (seen.has(k)) continue;
    seen.add(k);
    rules.push(`${p}.html ${p} 301!`);
  }
  if (!rules.length) return;
  writeFileSync(
    join(distDir, "_redirects"),
    [
      "# Generated by scripts/prerender.ts",
      "# Option B: prerender writes both <path>.html and <path>/index.html.",
      "# The clean URL resolves via the nested index.html. These rules only",
      "# matter on hosts that process _redirects (Lovable does not).",
      ...rules,
      "",
    ].join("\n"),
  );
}

function legacyRedirectStubHtml(targetPath: string) {
  const escaped = targetPath.replace(/"/g, "&quot;");
  return `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8" />
<title>Redirecting…</title>
<meta name="robots" content="noindex, follow" />
<meta http-equiv="refresh" content="0; url=${escaped}" />
<link rel="canonical" href="https://smartygym.com${escaped}" />
<script>window.location.replace(${JSON.stringify(targetPath)});</script>
</head>
<body>
<p>This page has moved. Redirecting to <a href="${escaped}">${escaped}</a>.</p>
</body>
</html>
`;
}

function writeLegacyRedirectStubs(
  distDir: string,
  routePaths: string[],
  redirects: Array<{ from: string; to: string; status: 301 }> = [],
) {
  // Lovable hosting does not honor Netlify-style `_redirects`. To get real
  // 301-equivalent behavior for legacy ID URLs, write a tiny static `.html`
  // file at every legacy path. The stub is `noindex, follow` with a meta
  // refresh + canonical pointing at the new slug URL. Google treats meta
  // refresh 0 with a canonical link as a redirect signal, and visitors get
  // moved instantly client-side.
  const canonicalPaths = new Set<string>();
  for (const p of routePaths) {
    if (p === "/") {
      canonicalPaths.add("/");
    } else {
      canonicalPaths.add(`${p}.html`);
      canonicalPaths.add(`${p}/index.html`);
    }
  }
  const written = new Set<string>();
  let count = 0;
  for (const rule of redirects) {
    if (rule.from === rule.to) continue;
    const target = canonicalPathFor(rule.to);
    const stubSources = new Set<string>();
    if (rule.from.endsWith(".html")) {
      stubSources.add(rule.from);
    } else {
      stubSources.add(`${rule.from}.html`);
      stubSources.add(`${rule.from}/index.html`);
    }
    for (const src of stubSources) {
      if (canonicalPaths.has(src)) continue; // never overwrite a real canonical page
      if (written.has(src)) continue;
      written.add(src);
      const outPath = join(distDir, src.replace(/^\//, ""));
      ensureParentDirectoryForFile(outPath);
      writeFileSync(outPath, legacyRedirectStubHtml(target));
      count++;
    }
  }
  if (count) console.log(`[prerender] wrote ${count} legacy redirect stubs`);
}

export async function prerenderSeoHtml(options: {
  distDir?: string;
  templatePath?: string;
} = {}) {
  const distDir = options.distDir || DIST;
  const templatePath = options.templatePath || join(distDir, "index.html");

  if (!existsSync(templatePath)) {
    throw new Error(
      `[prerender] dist/index.html not found at ${templatePath}. Run after Vite writes the HTML bundle.`,
    );
  }
  const template = normalizeTemplate(readFileSync(templatePath, "utf8"));

  const { routes, redirects, counts } = await buildSeoRoutes();
  console.log(
    `[prerender] rendering ${counts.total} routes (` +
      `static=${counts.static}, workout-cat=${counts.workoutCategory}, ` +
      `program-cat=${counts.programCategory}, workouts=${counts.workouts}, ` +
      `programs=${counts.programs}, blog=${counts.blogArticles})`,
  );

  let written = 0;
  const writeHtml = (outPath: string, html: string) => {
    ensureParentDirectoryForFile(outPath);
    writeFileSync(outPath, html);
    written++;
  };

  const allRoutePaths: string[] = [];
  const reportRows: string[] = [
    "# SmartyGym SEO prerender report",
    "",
    `Generated: ${new Date().toISOString()}`,
    `Routes: ${counts.total}`,
    "",
    "| URL | Static source file |",
    "| --- | --- |",
  ];
  for (const route of routes) {
    const { bodyHtml, jsonLd } = renderRouteBody(route);
    let html = applyHeadOverrides(template, route);
    html = injectJsonLd(html, jsonLd);
    html = injectBody(html, bodyHtml);

    if (route.path === "/") {
      writeHtml(join(distDir, "index.html"), html);
      reportRows.push("| / | dist/index.html |");
      allRoutePaths.push("/");
      continue;
    }

    // Option B: write the same prerendered HTML to BOTH the `.html`
    // sibling (kept for legacy links + hosts that prefer extensions) AND
    // the nested `index.html` (which lets the host resolve the clean URL
    // directly, without depending on `_redirects`).
    const cleanPath = route.path.replace(/^\//, "");
    const dotHtmlFile = join(distDir, `${cleanPath}.html`);
    const nestedIndexFile = join(distDir, cleanPath, "index.html");
    writeHtml(dotHtmlFile, html);
    writeHtml(nestedIndexFile, html);
    allRoutePaths.push(route.path);
    reportRows.push(
      `| ${route.path} | ${nestedIndexFile.replace(`${distDir}/`, "dist/")} (+ ${dotHtmlFile.replace(`${distDir}/`, "dist/")}) |`,
    );
  }
  writeCleanUrlRewrites(distDir, allRoutePaths, redirects);
  writeLegacyRedirectStubs(distDir, allRoutePaths, redirects);
  writeHeadersFile(distDir);
  writeFileSync(join(distDir, "seo-prerender-report.md"), `${reportRows.join("\n")}\n`);
  console.log(`[prerender] wrote ${written} HTML files into ${distDir.replace(process.cwd() + "/", "")}/`);
}

async function main() {
  await prerenderSeoHtml();
}

